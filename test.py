# Визначаємо функцію, яка виконує додавання
def simple_add(a, b):
    return a + b

# --- Основна частина програми ---

print("--- Програма для Простого Додавання ---")

try:
    # Отримання першого числа
    num1 = float(input("Введіть перше число: "))

    # Отримання другого числа
    num2 = float(input("Введіть друге число: "))

    # Виклик функції та відображення результату
    result = simple_add(num1, num2)

    # Вивід результату у форматі "5.5 + 3.2 = 8.7"
    print(f"Результат додавання: {num1} + {num2} = {result}")

except ValueError:
    print("Помилка: Ви ввели не число. Будь ласка, спробуйте ще раз.")
except Exception as e:
    print(f"Виникла несподівана помилка: {e}")

print("--------------------------------------")